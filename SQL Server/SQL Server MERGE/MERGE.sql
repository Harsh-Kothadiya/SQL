DROP TABLE IF EXISTS #SOURCE;

CREATE TABLE #SOURCE(
    ID INT,
    FIRST_NAME VARCHAR(20),
    LAST_NAME VARCHAR(20),
    DOB DATE,
    ROLE VARCHAR(50),
    HIGHEST_T20I_RUNS INT
);

INSERT INTO #SOURCE(ID,FIRST_NAME,LAST_NAME,DOB,ROLE,HIGHEST_T20I_RUNS)
SELECT 
    TRY_CAST(ID AS INT),
    FIRST_NAME,
    LAST_NAME,
    TRY_CAST(DOB AS DATE),
    ROLE,
    TRY_CAST(HIGHEST_T20I_RUNS AS INT)
FROM OPENJSON(@SOURCE_JSON)
WITH(
    ID VARCHAR(20),
    FIRST_NAME VARCHAR(20),
    LAST_NAME VARCHAR(20),
    DOB VARCHAR(20),
    ROLE VARCHAR(50),
    HIGHEST_T20I_RUNS VARCHAR(20)
);

