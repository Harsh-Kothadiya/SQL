MERGE CRICKETER_DETAIL AS T 
USING #SOURCE AS S
ON T.ID = S.ID
WHEN MATCHED
	THEN 
		UPDATE	
		SET
			T.FIRST_NAME = S.FIRST_NAME,
			T.LAST_NAME = S.LAST_NAME,
			T.DOB = S.DOB,
			T.ROLE = S.ROLE,
			T.HIGHEST_T20I_RUNS = S.HIGHEST_T20I_RUNS
WHEN NOT MATCHED
	THEN 
		INSERT (FIRST_NAME,LAST_NAME,DOB,ROLE,HIGHEST_T20I_RUNS)
		VALUES (S.FIRST_NAME,S.LAST_NAME,S.DOB,S.ROLE,S.HIGHEST_T20I_RUNS)
WHEN NOT MATCHED BY SOURCE
	THEN
		DELETE;





