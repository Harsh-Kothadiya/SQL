--PART:16 ONLY HIGHEST_T20I_RUNS COLUMN UPDATE VALUE SO (TRG_CRICKETER_DETAIL_HIGHEST_T20I_RUNS_UPDATE) TRIGGER FIRED
/*
	THIS TRIGGER DATA STORE TABLE: HIGHEST_T20I_RUNS_UPDATE_LOG
	VALUE CHANGE SO TRIGGER FIRED 
	SAME VALUE UPDATE SO NOT FIRED -- MY REQUIREMENT
*/
CREATE TABLE HIGHEST_T20I_RUNS_UPDATE_LOG(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	CDID INT,
	OLD_HIGHEST_T20I_RUNS INT,
	NEW_HIGHEST_T20I_RUNS INT,
	CREATEDAT DATETIME DEFAULT GETDATE()
)

CREATE TRIGGER TRG_CRICKETER_DETAIL_HIGHEST_T20I_RUNS_UPDATE
ON CRICKETER_DETAIL
AFTER UPDATE 
AS
BEGIN 
	IF UPDATE(HIGHEST_T20I_RUNS) --HIGHEST_T20I_RUNS ONLY THIS COLUMN VALUE CHANGE SO THIS TRIGGER FIRED
	BEGIN 
		IF EXISTS(
			SELECT 1 
			FROM INSERTED AS I 
			INNER JOIN DELETED AS D 
			ON I.CDID = D.CDID 
			WHERE   (I.HIGHEST_T20I_RUNS <> D.HIGHEST_T20I_RUNS)
                OR (I.HIGHEST_T20I_RUNS IS NULL AND D.HIGHEST_T20I_RUNS IS NOT NULL)
                OR (I.HIGHEST_T20I_RUNS IS NOT NULL AND D.HIGHEST_T20I_RUNS IS NULL)
		)
		BEGIN 
			PRINT 'TRIGGER FIRED: TRG_CRICKETER_DETAIL_HIGHEST_T20I_RUNS_UPDATE.'
			
			INSERT INTO HIGHEST_T20I_RUNS_UPDATE_LOG(CDID,OLD_HIGHEST_T20I_RUNS,NEW_HIGHEST_T20I_RUNS)
			SELECT I.CDID,D.HIGHEST_T20I_RUNS,I.HIGHEST_T20I_RUNS
			FROM INSERTED AS I 
			INNER JOIN DELETED AS D 
			ON I.CDID = D.CDID 
			WHERE    (I.HIGHEST_T20I_RUNS <> D.HIGHEST_T20I_RUNS)
                OR (I.HIGHEST_T20I_RUNS IS NULL AND D.HIGHEST_T20I_RUNS IS NOT NULL)
                OR (I.HIGHEST_T20I_RUNS IS NOT NULL AND D.HIGHEST_T20I_RUNS IS NULL)	
		END
	END
END

