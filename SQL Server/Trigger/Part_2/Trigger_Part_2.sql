CREATE TRIGGER TRG_CRICKETER_DETAIL_UPDATEDAT
ON CRICKETER_DETAIL
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;

    UPDATE C
    SET UPDATEDAT = GETDATE()
    FROM CRICKETER_DETAIL C
    INNER JOIN INSERTED I
        ON C.CDID = I.CDID;
		
	PRINT 'TRIGGER FIRED: TRG_CRICKETER_DETAIL_UPDATEDAT.'
END;

