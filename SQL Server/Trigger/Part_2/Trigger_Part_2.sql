*/
CREATE TABLE DELETE_LOG(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	COLUMN_LIST NVARCHAR(MAX),
	CREATEDAT DATETIME DEFAULT GETDATE()
)

CREATE TRIGGER TRG_CRICKETER_DETAIL_DELETE
ON CRICKETER_DETAIL
AFTER DELETE 
AS
BEGIN 
	PRINT 'TRIGGER FIRED: TRG_CRICKETER_DETAIL_DELETE.'
	
	INSERT INTO DELETE_LOG (COLUMN_LIST)
	SELECT (
		SELECT * 
		FROM DELETED
		FOR JSON PATH
	) AS COLUMN_LIST;
END

