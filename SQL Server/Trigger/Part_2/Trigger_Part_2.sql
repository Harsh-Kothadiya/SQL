--PART:15 ONLY HIGHEST_T20I_RUNS COLUMN INSERT VALUE SO (TRG_CRICKETER_DETAIL_HIGHEST_T20I_RUNS_INSERT) TRIGGER FIRED
/*
	THIS TRIGGER DATA STORE TABLE: HIGHEST_T20I_RUNS_INSERT_LOG
*/
CREATE TABLE HIGHEST_T20I_RUNS_INSERT_LOG(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	CDID INT,
	HIGHEST_T20I_RUNS INT,
	CREATEDAT DATETIME DEFAULT GETDATE()
)


CREATE TRIGGER TRG_CRICKETER_DETAIL_HIGHEST_T20I_RUNS_INSERT
ON CRICKETER_DETAIL
AFTER INSERT 
AS
BEGIN 
	IF EXISTS(
		SELECT 1 
		FROM INSERTED
		WHERE HIGHEST_T20I_RUNS IS NOT NULL
	)
	BEGIN 
		PRINT 'TRIGGER FIRED: TRG_CRICKETER_DETAIL_HIGHEST_T20I_RUNS_INSERT.'
		
		INSERT INTO HIGHEST_T20I_RUNS_INSERT_LOG(CDID,HIGHEST_T20I_RUNS)
		SELECT CDID,HIGHEST_T20I_RUNS FROM INSERTED
	END
END

