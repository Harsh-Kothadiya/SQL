--ANY COLUMN UPDATE 

CREATE TABLE CRICKETER_DETAIL_ANY_COLUMN_UPDATE_LOG(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	CDID INT,
	COLUMNNAME VARCHAR(100),
	OLDVALUE VARCHAR(MAX), 
	NEWVALUE VARCHAR(MAX),
	CREATEDAT DATETIME DEFAULT GETDATE()
)


CREATE TRIGGER TRG_CRICKETER_DETAIL_ANY_COLUMN_UPDATE
ON CRICKETER_DETAIL
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @sql NVARCHAR(MAX);

    -- Move pseudo tables
    SELECT * INTO #I FROM INSERTED;
    SELECT * INTO #D FROM DELETED;

    -- Build dynamic SQL safely
    SELECT @sql = '
    INSERT INTO CRICKETER_DETAIL_ANY_COLUMN_UPDATE_LOG (CDID, COLUMNNAME, OLDVALUE, NEWVALUE)
    SELECT *
    FROM (
        ' + STRING_AGG(
            '
            SELECT 
                I.CDID,
                ''' + c.name + ''' AS COLUMNNAME,
                CONVERT(NVARCHAR(MAX), D.' + QUOTENAME(c.name) + ') AS OLDVALUE,
                CONVERT(NVARCHAR(MAX), I.' + QUOTENAME(c.name) + ') AS NEWVALUE
            FROM #I I
            JOIN #D D ON I.CDID = D.CDID
            ',
            ' UNION ALL '
        ) + '
    ) A
    WHERE ISNULL(OLDVALUE, '''') <> ISNULL(NEWVALUE, '''');
    '
    FROM sys.columns c
    WHERE c.object_id = OBJECT_ID('CRICKETER_DETAIL')
      AND c.name NOT IN ('CDID','CREATEDAT')
      AND c.is_computed = 0;

    -- DEBUG (optional)
    -- PRINT @sql;

    EXEC sp_executesql @sql;

    PRINT 'TRIGGER FIRED: TRG_CRICKETER_DETAIL_ANY_COLUMN_UPDATE.'

END;

