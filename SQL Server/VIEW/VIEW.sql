QUE: What is a View in SQL Server?
ANS:
--One type of sql query.
--Views as virtual tables.
--Views do not store any data physically by default.

QUE: What are the differences between a table and a view in SQL Server?
ANS:
/*
--The table is physical whereas the view is logical or virtual.
--A Table is an independent object whereas a view is a dependent object that is a view depends on the table or tables from which it is loading the data.
--When a new table is created from an existing table the new and old tables are independent themselves that is the changes of one table will not be reflected into the other table 
whereas if a view is created based on a table any changes that are performed on the table reflects into the view and any changes performed on the view reflected in the table also.
*/

