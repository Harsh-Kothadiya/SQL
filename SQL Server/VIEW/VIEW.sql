CREATE PROCEDURE SP_DROP_TABLE_WITH_VIEWS
    @TableName SYSNAME
AS
BEGIN
    DECLARE @sql NVARCHAR(MAX) = '';

    -- Drop dependent views first
    SELECT @sql = @sql + 'DROP VIEW ' 
        + QUOTENAME(SCHEMA_NAME(v.schema_id)) 
        + '.' + QUOTENAME(v.name) + ';' + CHAR(13)
    FROM sys.views v
    INNER JOIN sys.sql_expression_dependencies d 
        ON v.object_id = d.referencing_id
    WHERE d.referenced_entity_name = @TableName;

    EXEC sp_executesql @sql;

    -- Drop table
    SET @sql = 'DROP TABLE ' + QUOTENAME(@TableName);
    EXEC sp_executesql @sql;
END;

